      name = defaultImage;
    }
    final size = _svgImageSize[name];
    if (size != null) {
      return SvgPicture.asset(
        name,
        width: width ?? size[0],
        height: height ?? size[1],
        color: color,
      );
    }
    return SvgPicture.asset(
      name,
      color: color,
      width: width,
      height: height,
    );
